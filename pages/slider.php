<script>
    document.addEventListener('DOMContentLoaded', () => {
        const slider = document.getElementById('blog-slider');
        const originalCardsContainer = document.getElementById('original-cards');
        
        if (!slider || !originalCardsContainer) return;

        // 1. DUPLICATE CARDS FOR INFINITE LOOP EFFECT
        const originalContent = originalCardsContainer.innerHTML;
        // Clone the content multiple times for a better loop effect before removal
        slider.innerHTML += originalContent + originalContent + originalContent;
        
        // Remove the original container after duplication
        originalCardsContainer.remove();


        // 2. INFINITE LOOP CHECK LOGIC
        // We calculate the width of ONE set of the original cards
        const singleSetWidth = slider.scrollWidth / 4; // Assuming 4 sets: 1 original + 3 clones
        
        // This function executes every time the user scrolls
        slider.addEventListener('scroll', () => {
            // When the user has scrolled one full set (or more)
            if (slider.scrollLeft >= singleSetWidth) {
                // Instantly reset scroll position to the start of the next set
                // We shift by one set width to keep the illusion going
                slider.scrollLeft -= singleSetWidth;
            } else if (slider.scrollLeft === 0) {
                 // If the user scrolls back to the very start (Card 1 of the first set)
                 // Jump to the equivalent position at the start of the second set 
                 // (where the loop naturally starts) to allow infinite reverse scroll.
                 slider.scrollLeft = singleSetWidth; 
            }
        });


        // 3. MOUSE DRAGGING & TOUCH SWIPING LOGIC
        let isDown = false;
        let startX;
        let scrollLeft;
        
        // MOUSE EVENTS
        slider.addEventListener('mousedown', (e) => {
            isDown = true;
            slider.classList.add('active'); 
            startX = e.pageX - slider.offsetLeft;
            scrollLeft = slider.scrollLeft;
        });

        slider.addEventListener('mouseleave', () => {
            isDown = false;
            slider.classList.remove('active');
        });

        slider.addEventListener('mouseup', () => {
            isDown = false;
            slider.classList.remove('active');
        });

        slider.addEventListener('mousemove', (e) => {
            if (!isDown) return;
            e.preventDefault();
            const x = e.pageX - slider.offsetLeft;
            const walk = (x - startX) * 1.5; 
            slider.scrollLeft = scrollLeft - walk;
        });
        
        // TOUCH EVENTS
        slider.addEventListener('touchstart', (e) => {
            isDown = true;
            startX = e.touches[0].pageX - slider.offsetLeft;
            scrollLeft = slider.scrollLeft;
        });

        slider.addEventListener('touchend', () => {
            isDown = false;
        });

        slider.addEventListener('touchmove', (e) => {
            if (!isDown) return;
            const x = e.touches[0].pageX - slider.offsetLeft;
            const walk = (x - startX) * 1.5;
            slider.scrollLeft = scrollLeft - walk;
        });
    });
</script>